metres = int(input("Введите количество метров: "))
centimeters = int(metres * 100)
decimeters = int(metres * 10)
millimetres = int(metres * 1000)

print(f"В вашем числе {centimeters} сантиметров.")
print(f"В вашем числе {decimeters} дециметров.")
print(f"В вашем числе {millimetres} миллиметров.")
